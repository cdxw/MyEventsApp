using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace myEventsApp
{
    class FormValidator
    {
        public bool isValid(string value)
        {
            bool isValidValue = false;

            if (value != null)
            {
                return isValidValue = true;
            }else
            return isValidValue;
        }
    }
}
